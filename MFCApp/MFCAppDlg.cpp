// CMFCAppDlg 对话框



CMFCAppDlg::CMFCAppDlg(CWnd* pParent /*=nullptr*/)
	: CDialogEx(IDD_MFCAPP_DIALOG, pParent)
	, mstr_Author(_T(""))
	, mstr_Ip(_T(""))
	, mstr_Name(_T(""))
{
	m_hIcon = AfxGetApp()->LoadIcon(IDR_MAINFRAME);
}

void CMFCAppDlg::DoDataExchange(CDataExchange* pDX)
{
	CDialogEx::DoDataExchange(pDX);
	DDX_Text(pDX, IDC_STATIC_AUTHOR, mstr_Author);
	DDX_Text(pDX, IDC_EDIT2, mstr_Ip);
	DDX_Text(pDX, IDC_EDIT1, mstr_Name);
}

